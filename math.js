// Math
//standard built-in objects - always available


// rounds up the value
const number = 4.45678;
const result = Math.floor(number); //floor() it rounds up a number

console.log(result);


// adds up 1 one to the 7
const number2 = 7.11111;
const result2 = Math.ceil(number2);// ceil() adds up to the rounded up number

console.log(result2);

// this does the squareroot
const number3 = 1024;
const result3 = Math.sqrt(number3);

console.log(result3);

// This shows the pi
const result4 = Math.PI;
console.log(result4);

// Gives the minimum value

const result5 = Math.min(4, 5, 6, 7, 8, 9, 100, 999);

//Gives maximum number
const result6 = Math.max(4, 5, 6, 7, 8, 9, 100, 999);

console.log(result5);
console.log(result6);

//random gives random numbers(it can never be 1. but will always start with 0.182 or any number)

const result7 = Math.random();
console.log(result7);
